// ─── Engine ──────────────────────────────────────────────────
let _lang = localStorage.getItem('uc_lang') || 'en';

function t(key, params) {
  let s = (LANGS[_lang] && LANGS[_lang][key]) || LANGS.en[key] || key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      s = s.replaceAll('{' + k + '}', v);
    }
  }
  return s;
}

function getLang() { return _lang; }

function setLang(lang) {
  if (!LANGS[lang]) return;
  _lang = lang;
  localStorage.setItem('uc_lang', lang);
  document.documentElement.lang = lang;
  applyLang();
}

function toggleLang() {
  setLang(_lang === 'en' ? 'fr' : 'en');
}

function applyLang() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const attr = el.getAttribute('data-i18n-attr');
    const val = t(key);
    if (attr === 'placeholder') el.placeholder = val;
    else if (attr === 'title') el.title = val;
    else if (el.getAttribute('data-i18n-html') !== null) el.innerHTML = val;
    else el.textContent = val;
  });
  // Dispatch event so pages can update dynamic content
  window.dispatchEvent(new Event('langchange'));
}

// Language toggle button injection
function injectLangToggle() {
  if (document.getElementById('lang-toggle')) return;
  const btn = document.createElement('button');
  btn.id = 'lang-toggle';
  btn.style.cssText = 'position:fixed;top:12px;right:12px;z-index:9999;background:rgba(18,18,26,0.85);backdrop-filter:blur(8px);border:1px solid rgba(255,255,255,0.12);border-radius:8px;color:#f0f0f8;font-family:inherit;font-size:13px;font-weight:600;padding:6px 12px;cursor:pointer;transition:all 0.15s;letter-spacing:0.03em;';
  btn.textContent = _lang === 'en' ? '🇫🇷 FR' : '🇬🇧 EN';
  btn.addEventListener('click', () => {
    toggleLang();
    btn.textContent = _lang === 'en' ? '🇫🇷 FR' : '🇬🇧 EN';
  });
  btn.addEventListener('mouseenter', () => { btn.style.borderColor = '#e8375a'; btn.style.color = '#e8375a'; });
  btn.addEventListener('mouseleave', () => { btn.style.borderColor = 'rgba(255,255,255,0.12)'; btn.style.color = '#f0f0f8'; });
  document.body.appendChild(btn);
}

// Auto-init on load
document.addEventListener('DOMContentLoaded', () => {
  document.documentElement.lang = _lang;
  injectLangToggle();
  applyLang();
});

// Export to global scope
window.t = t;
